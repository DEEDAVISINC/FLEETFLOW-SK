// Helper functions for filtering and searching
export function getLRAFSourcesByTier(tier: LRAFSource['tier']): LRAFSource[] {
  return LRAF_SOURCES.filter((source) => source.tier === tier);
}

export function getTransportationRelevantSources(): LRAFSource[] {
  return LRAF_SOURCES.filter((source) => source.transportationRelevant);
}

export function getCriticalPrioritySources(): LRAFSource[] {
  return LRAF_SOURCES.filter((source) => source.priority === 'critical');
}

export function searchLRAFSources(query: string): LRAFSource[] {
  const lowercaseQuery = query.toLowerCase();
  return LRAF_SOURCES.filter(
    (source) =>
      source.agency.toLowerCase().includes(lowercaseQuery) ||
      source.agencyCode.toLowerCase().includes(lowercaseQuery) ||
      source.description.toLowerCase().includes(lowercaseQuery) ||
      source.category.toLowerCase().includes(lowercaseQuery)
  );
}

export function getLRAFSourceById(id: string): LRAFSource | undefined {
  return LRAF_SOURCES.find((source) => source.id === id);
}
