// Handles business workflows like quote acceptance, contract generation, invoice approval
// Complements the existing operational workflow system for driver/load operations

import { logger } from '../utils/logger';

export interface BusinessWorkflowStep {
  id: string;
  name: string;
  description: string;
  required: boolean;
  completed: boolean;
  completedAt?: string;
  completedBy?: string;
  data?: any;
  allowOverride?: boolean;
  overrideReason?: string;
  overrideBy?: string;
}

export interface BusinessWorkflow {
  id: string;
  type:
    | 'quote_acceptance'
    | 'contract_generation'
    | 'invoice_approval'
    | 'payment_processing'
    | 'compliance_review'
    | 'load_assignment'
    | 'carrier_onboarding'
    | 'shipper_onboarding';
  status:
    | 'pending'
    | 'in_progress'
    | 'completed'
    | 'cancelled'
    | 'override_required';
  currentStep: number;
  steps: BusinessWorkflowStep[];
  createdAt: string;
  updatedAt: string;
  metadata?: any;
}

export type BusinessWorkflowStepId =
  // Quote Acceptance Workflow
  | 'quote_generated'
  | 'quote_sent_to_shipper'
  | 'quote_reviewed_by_shipper'
  | 'quote_accepted_by_shipper'
